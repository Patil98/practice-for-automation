package Bhushan;

public class swaping
{
	public static void main(String[] args) 
	{
		

	int a=10;
	int b=20;
	
	
	System.out.println("before swaping num is "+ a + " and " + b );
	
	a=a+b;
	b=a-b;
	a=a-b;
	
	
	System.out.println("after swaping num is "+ a + " and " + b );
	

}
}