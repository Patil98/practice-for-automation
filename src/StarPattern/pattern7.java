package StarPattern;

public class pattern7 

{
	
	public static void main(String[] args)
	
	{
		// half pyramid with number
		/*
		1
		12
		123
		1234
		12345
	    */
		
		
		for(int i=1; i<=5; i++)
		{
			for(int j=1; j<=i; j++)
			{
				System.out.print(j+"");
			}
			System.out.println();
			
		}
		
		
	}

}
