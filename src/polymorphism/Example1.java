package polymorphism;

public class Example1        // method overloading

{
	// multiplication with 2 int parameter
	public void mult(int num1, int num2)
	{
		System.out.println(num1*num2);
		
	}
	// multiplication with 3 int parameter
	public void mult(int num1,int num2, int num3)
	{
		System.out.println(num1*num2*num3);
	}
	
	

}
