package Array;

public class Example9       //2*2  multi dimensional String  Array
{
	
	public static void main(String[] args) 
	{
		String ar[][]= {{"bhushan","atul"},{"amol","aniket"}};
	
	for(int i=0; i<=1; i++)
	{
		for(int j=0; j<=1; j++)
		{
			System.out.print(ar[i][j]+"  ");
		}
		System.out.println();
			
	}
	
	
	
	
	}
}
